A = str(input('\033[33mDigite algo: '))
print('\033[1;35mO tipo primitivo desse valor é''\033[m', type(A))
print('\033[1;35mSó tem espaço? ''\033[m', A.isspace())
print('\033[1;35mÉ um número? ''\033[m', A.isnumeric())
print('\033[1;35mÉ alfabético? ''\033[m', A.isalpha())
print('\033[1;35mÉ alfanuérico? ''\033[m', A.isalnum())
print('\033[1;35mEstá em minúsculas? ''\033[m', A.islower())
print('\033[1;35mEstá em maiúsculas? ''\033[m',A.isupper())
print('\033[1;35mEstá capitalizada? ''\033[m', A.istitle())
